# Working with Integer DataTypes

a = 1056337335677636665633 # Normal Integer

print(a)    # printing the normal integer by default it is decimal

print(bin(a))   # printing the binary integer

binary = 0b1111 # Binary Number

print(binary)

octal = 0o32342

print(octal)    # Printing the octal number

print(oct(10))  # Creating the octal number

hexaDecimal = 0xAF  # Creating the HexaDecimal Integer

print(hexaDecimal)

print(hex(175)) # prints the HexaDecimal Value
